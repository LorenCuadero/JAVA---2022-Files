    private void jButtonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterActionPerformed
        String fname = jfname.getText();
        String lname = jlname.getText();
        String uname = juname.getText();
        String pass = String.valueOf(jpass.getPassword());
        String re_pass = String.valueOf(jrepass.getPassword());
        String address = jadd.getText();

        if (uname.equals("")) {
            JOptionPane.showMessageDialog(null, "Add A Username");
        } else if (pass.equals("")) {
            JOptionPane.showMessageDialog(null, "Add A Password");
        } else if (fname.equals("")) {
            JOptionPane.showMessageDialog(null, "Add Your First Name");
        } else if (lname.equals("")) {
            JOptionPane.showMessageDialog(null, "Add Your Last Name");
        } else if (address.equals("")) {
            JOptionPane.showMessageDialog(null, "Add Your Address");
        } else if (!pass.equals(re_pass)) {
            JOptionPane.showMessageDialog(null, "Retype The Password Again");
        } else if (checkUsername(uname)) {
            JOptionPane.showMessageDialog(null, "This Username Already Exist");
        } else {

            PreparedStatement ps;
            String query = "INSERT INTO `login_users`(`First_name`, `Last_name`, `Username`, `Password`, `Address`) VALUES (?,?,?,?,?)";

            try {
                ps = DURC.getConnection().prepareStatement(query);

                ps.setString(1, fname);
                ps.setString(2, lname);
                ps.setString(3, uname);
                ps.setString(4, pass);
                ps.setString(5, address);

                if (ps.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(null, "New User Added");
                }

            } catch (SQLException ex) {
//            Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButtonRegisterActionPerformed

