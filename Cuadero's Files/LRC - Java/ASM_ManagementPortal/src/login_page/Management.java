    private void jview4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jview4ActionPerformed
        String id = jid.getText();
        String fname = jfname.getText();
        String lname = jlname.getText();
        String uname = juname.getText();
        String pass = jpass.getText();
        String add = jadd.getText();
        String pos = jpos.getText();

        if (pos.equals("")) {
            JOptionPane.showMessageDialog(null, "Add An Employees Position");
        } else if (fname.equals("")) {
            JOptionPane.showMessageDialog(null, "Add An Employees Name");
        } else if (uname.equals("")) {
            JOptionPane.showMessageDialog(null, "Add An Employees Username");
        } else if (pass.equals("")) {
            JOptionPane.showMessageDialog(null, "Add An Employees Password");
        } else if (add.equals("")) {
            JOptionPane.showMessageDialog(null, "Add An Employees Address");
        } else {
            PreparedStatement ps;
            String query = "INSERT INTO `users`(`First_name`,`Last_name`,`Username`,`Password`,`Address`,`Type_of_User`) VALUES (?,?,?,?,?,?)";
            try {
                ps = DURC.getConnection().prepareStatement(query);

                ps.setString(1, fname);
                ps.setString(2, lname);
                ps.setString(3, uname);
                ps.setString(4, pass);
                ps.setString(5, add);
                ps.setString(6, pos);

                if (ps.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(null, "New Employee Added");
                }

            } catch (SQLException ex) {
                System.out.println("Error : " + ex.getMessage());
            }
        }
    }

