    public boolean checkBar(String id) {
        PreparedStatement ps;
        ResultSet rs;
        boolean checkUser = false;
        String query = "SELECT * FROM `users` WHERE `Users_Id` =?";
        try {
            ps = DURC.getConnection().prepareStatement(query);
            ps.setString(1, id);

            rs = ps.executeQuery();

            if (rs.next()) {
                checkUser = true;
            }
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(DURC.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
        return checkUser;
    }

